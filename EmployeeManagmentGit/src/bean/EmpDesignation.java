package bean;

public class EmpDesignation 
{
int id;
String Type;
String Name;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getType() {
	return Type;
}
public void setType(String type) {
	Type = type;
}
public String getName() {
	return Name;
}
public void setName(String name) {
	Name = name;
}


}
